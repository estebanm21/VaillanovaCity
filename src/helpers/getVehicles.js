export function getOneCar(id) {
  let newId = Number(id);
  if (isNaN(newId)) {
    console.error("el id proporcionado no es un numero valido");
    return null;
  }
  return cars.find((car) => {
    if (car.id === newId) {
      return car;
    }
  });
}
